    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String fr = (String) jTextField1.getText();
        String ket = (String) jComboBox1.getSelectedItem();
        
        if(fr.equals("")|| ket.equals("Pilih")){
            JOptionPane.showMessageDialog(null, "Isi data dulu");
        }else if (ket.equals("Pilih")){
            JOptionPane.showMessageDialog(null, "Isi data dulu");
        }else{
        try{
                String Q = "INSErT INTO Frame (Frame,Keterangan) VALUES ('"+fr+"','"+ket+"')";
                java.sql.Connection Vconn = (Connection)KoneksiDB.KonfigDB();
                java.sql.PreparedStatement s = Vconn.prepareStatement(Q);
                s.execute();
                JOptionPane.showMessageDialog(null, "Tambah Data Berhasil");
                
            }catch(Exception e){
                JOptionPane.showMessageDialog(null,"Tambah Data gagal");
                JOptionPane.showMessageDialog(this, e.getMessage());
            }
        tampil_tabel();
        bersih();
        }
    }//GEN-LAST:event_jButton2ActionPerformed

