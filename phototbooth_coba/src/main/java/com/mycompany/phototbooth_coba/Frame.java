    private void tampil_tabel()
    {
        //Membuat objek pada tabel
        DefaultTableModel tb = new DefaultTableModel();
        tb.addColumn("No");
        tb.addColumn("Nama Frame");
        tb.addColumn ("Keterangan");
        
        
        try{
            int counter = 1;
            String query = "SELECT * FROM Frame";
            
            java.sql.Connection vconn = (Connection)KoneksiDB.KonfigDB();
            
            java.sql.Statement s = vconn.createStatement(); 
            
            java.sql.ResultSet r = s.executeQuery(query);
            
            while(r.next()){
                tb.addRow(new Object[]{
                    counter++,r.getString(2),r.getString(3)
                });
            }
            Tabel_frame.setModel(tb);
        }catch(Exception e){
        }
    }
    
